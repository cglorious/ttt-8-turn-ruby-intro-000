def input_to_index(user_input)
  user_input.to_i - 1
end

def move(board, index, current_player = "X")
  board[index] = current_player
end

def turn (get input)
  convert input to index
  if index is valid
    make the move for input
  else
    ask for input again until you get a valid input
  end
end
